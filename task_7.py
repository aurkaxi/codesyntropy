num = int(input("Enter your number: "))

if num >= 80:
    print("you got A+")
elif num >= 70:
    print("you got A")
elif num >= 60:
    print("you got A-")
elif num >= 50:
    print("you got B")
elif num >= 40:
    print("you got C")
elif num >= 33:
    print("you got D")
else:
    print("you got F")
